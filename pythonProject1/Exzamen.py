#  Напишіть програму, яка приймає два цілих числа від
# користувача і виводить суму діапазону чисел між ними
# Отримуємо два цілі числа від користувача
a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))
start = min(a, b)
end = max(a, b)
total_sum = sum(range(start, end + 1))
print("Сума діапазону чисел між", a, "і", b, "дорівнює", total_sum)
